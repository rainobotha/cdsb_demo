        "CREATE OR REPLACE TABLE SERVICE_REQUEST_EVENTS AS\n",
        "WITH cases AS (\n",
        "    SELECT\n",
        "        SEQ4() + 1 as CASE_ID,\n",
        "        DATEADD('second', UNIFORM(0, 20000000, RANDOM()), '2025-08-01'::TIMESTAMP) as BASE_TIME,\n",
        "        ARRAY_CONSTRUCT('Registration Change','Payment Query','Complaint',\n",
        "            'Information Request','Licence Application','Permit Application'\n",
        "        )[UNIFORM(0, 5, RANDOM())]::VARCHAR as REQUEST_TYPE,\n",
        "        ARRAY_CONSTRUCT('Phone','Web Portal','Email','In-Person'\n",
        "        )[UNIFORM(0, 3, RANDOM())]::VARCHAR as CHANNEL,\n",
        "        ARRAY_CONSTRUCT('Brisbane','Gold Coast','Sunshine Coast','Townsville',\n",
        "            'Cairns','Toowoomba','Ipswich','Mackay'\n",
        "        )[UNIFORM(0, 7, RANDOM())]::VARCHAR as REGION,\n",
        "        ARRAY_CONSTRUCT('Handler_A','Handler_B','Handler_C','Handler_D','Handler_E'\n",
        "        )[UNIFORM(0, 4, RANDOM())]::VARCHAR as HANDLER,\n",
        "        UNIFORM(1, 100, RANDOM()) as FATE\n",
        "    FROM TABLE(GENERATOR(ROWCOUNT => 10000))\n",
        "),\n",
        "events AS (\n",
        "    SELECT CASE_ID, 'Submitted' as ACTIVITY, BASE_TIME as EVENT_TIME,\n",
        "           REQUEST_TYPE, CHANNEL, REGION, 'System' as ACTOR FROM cases\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Triaged',\n",
        "           DATEADD('minute', UNIFORM(30, 180, RANDOM()), BASE_TIME),\n",
        "           REQUEST_TYPE, CHANNEL, REGION, 'Triage_Team' FROM cases WHERE FATE > 3\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Assigned',\n",
        "           DATEADD('minute', UNIFORM(90, 360, RANDOM()), BASE_TIME),\n",
        "           REQUEST_TYPE, CHANNEL, REGION, HANDLER FROM cases WHERE FATE > 5\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'In Progress',\n",
        "           DATEADD('hour', UNIFORM(6, 72, RANDOM()), BASE_TIME),\n",
        "           REQUEST_TYPE, CHANNEL, REGION, HANDLER FROM cases WHERE FATE > 8\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Escalated',\n",
        "           DATEADD('hour', UNIFORM(24, 120, RANDOM()), BASE_TIME),\n",
        "           REQUEST_TYPE, CHANNEL, REGION, 'Escalation_Manager' FROM cases WHERE FATE BETWEEN 15 AND 30\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Resolved',\n",
