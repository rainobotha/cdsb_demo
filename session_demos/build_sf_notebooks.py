meta = {
    "kernelspec": {"display_name": "Streamlit Notebook", "name": "streamlit"}
}

def md(name, lines):
    return {"cell_type": "markdown", "id": name, "metadata": {"collapsed": False, "name": name}, "source": lines}

def py(name, lines):
    return {"cell_type": "code", "execution_count": None, "id": name, "metadata": {"codeCollapsed": False, "language": "python", "name": name}, "outputs": [], "source": lines}

def sql(name, lines):
    return {"cell_type": "code", "execution_count": None, "id": name, "metadata": {"codeCollapsed": False, "language": "sql", "name": name}, "outputs": [], "source": lines}

def save(name, cells):
    nb = {"cells": cells, "metadata": meta, "nbformat": 4, "nbformat_minor": 5}
    raw = json.dumps(nb, indent=1)
    raw = raw.replace("CDSB_DEMO", DATABASE)
    raw = raw.replace("COMPUTE_WH", WAREHOUSE)
    raw = raw.replace("SE_SNOW_NEO4J_GRAPH_ANALYTICS", NEO4J_APP)
    nb = json.loads(raw)
    path = os.path.join(base, name)
    with open(path, 'w') as f:
        json.dump(nb, f, indent=1)
    ct = {"markdown": 0, "sql": 0, "python": 0}
    for c in cells:
        lang = c.get("metadata", {}).get("language", "")
        if c["cell_type"] == "markdown":
            ct["markdown"] += 1
        elif lang == "sql":
            ct["sql"] += 1
        else:
            ct["python"] += 1
    print(f'{name}: {len(cells)} cells ({ct["markdown"]} md, {ct["python"]} py, {ct["sql"]} sql)')


