        "           REQUEST_TYPE, CHANNEL, REGION, HANDLER FROM cases WHERE FATE > 10\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Reopened',\n",
        "           DATEADD('hour', UNIFORM(100, 400, RANDOM()), BASE_TIME),\n",
        "           REQUEST_TYPE, CHANNEL, REGION, HANDLER FROM cases WHERE FATE BETWEEN 85 AND 95\n",
        "    UNION ALL\n",
        "    SELECT CASE_ID, 'Closed',\n",
